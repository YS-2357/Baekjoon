# ✅ [✔ 모범 답안: 올바르게 수정된 코드]
# - `itertools.combinations()`을 사용하여 아홉 명 중 일곱 명을 선택하는 모든 경우 탐색
# - `sum()`을 이용하여 키의 합이 100인 조합을 찾음
# - `sorted()`를 사용하여 오름차순 출력

import sys
import itertools

# 난쟁이 키 입력 받기
array = [int(sys.stdin.readline().strip()) for _ in range(9)]

# 아홉 명 중 일곱 명을 선택하는 모든 조합 탐색
for seven_dwarfs in itertools.combinations(array, 7):
    if sum(seven_dwarfs) == 100:  # 합이 100인 조합 찾기
        for height in sorted(seven_dwarfs):  # 오름차순 정렬하여 출력
            print(height)
        break  # 정답이 하나만 존재하므로 찾으면 종료
