# 백준 9095번: 1, 2, 3 더하기
# 문제 설명:
# 정수 n을 1, 2, 3의 합으로 나타내는 방법의 개수를 구하는 문제.
# - 같은 숫자가 다른 순서로 나타나는 경우도 다른 방법으로 취급한다.
# - 재귀와 메모이제이션을 이용해 해결 가능.

# 입력 형식:
# - 첫 번째 줄에 테스트 케이스 개수 T가 주어진다.
# - 각 테스트 케이스마다 정수 n이 주어진다. (1 ≤ n ≤ 10)

# 출력 형식:
# - 각 테스트 케이스마다 가능한 방법의 개수를 출력한다.

# 예제 입력 1:
# 3
# 4
# 7
# 10
# 예제 출력 1:
# 7
# 44
# 274

import sys

# ✅ 입력 처리
T = int(sys.stdin.readline())
nums = [int(sys.stdin.readline()) for _ in range(T)]

# ✅ 메모이제이션을 위한 딕셔너리 (기본값 저장)
memo = {0: 1, 1: 1, 2: 2, 3: 4}

# ✅ 재귀 + 메모이제이션 (탑다운 방식)
def trimino(n):
    if n in memo:  # 이미 계산된 값이면 반환
        return memo[n]
    
    memo[n] = trimino(n-1) + trimino(n-2) + trimino(n-3)
    return memo[n]

# ✅ 결과 출력
for num in nums:
    print(trimino(num))

